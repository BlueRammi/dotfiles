# Scheme switcher for terminal
#
# DESCRIPTION:
# This script alternates two color schemes in terminal depending on the hour of the day.

function scheme-switcher() {
	if [[ ($(date +%H) -ge 8) && ($(date +%H) -lt 20) ]]; then
		DARK='\033]10;#A6B5C5\007\033]11;[90]#040506\007\033]708;[90]#040506\007\033]4;0;[90]#040506\007\033]4;1;#EC5252\007\033]4;2;#5DA813\007\033]4;3;#C97016\007\033]4;4;#3790E9\007\033]4;5;#A35AED\007\033]4;6;#13AEAE\007\033]4;7;#53697F\007\033]4;8;#0E1216\007\033]4;9;#FF9191\007\033]4;10;#9ACD68\007\033]4;11;#EDA45C\007\033]4;12;#7DBEFF\007\033]4;13;#CA96FF\007\033]4;14;#6ECFCF\007\033]4;15;#A6B5C5\007\033]12;#A6B5C5\007'
	else
		DARK='\033]10;#C0AF9F\007\033]11;[90]#060504\007\033]708;[90]#060504\007\033]4;0;[90]#060504\007\033]4;1;#EC5252\007\033]4;2;#5DA813\007\033]4;3;#C97016\007\033]4;4;#3790E9\007\033]4;5;#A35AED\007\033]4;6;#13AEAE\007\033]4;7;#76614D\007\033]4;8;#14110D\007\033]4;9;#FF9191\007\033]4;10;#9ACD68\007\033]4;11;#EDA45C\007\033]4;12;#7DBEFF\007\033]4;13;#CA96FF\007\033]4;14;#6ECFCF\007\033]4;15;#C0AF9F\007\033]12;#C0AF9F\007'
	fi

	printf "$DARK"
}

scheme-switcher
